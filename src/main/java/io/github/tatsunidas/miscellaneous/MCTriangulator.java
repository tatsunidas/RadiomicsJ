	private List<Point3f> vertices = new ArrayList<>();

	public List<Point3f> getTriangles(ImagePlus imp, double threshold, int resamplingF) {
		vertices.clear();

		// 1. 飛ばし読み（step）ではなく、画像を実際にリサンプルする
		ImagePlus processedImp = imp;
		if (resamplingF > 1) {
			// 画像を 1/resamplingF に縮小（平均化を伴う）
			int newW = Math.max(1, imp.getWidth() / resamplingF);
			int newH = Math.max(1, imp.getHeight() / resamplingF);
			int newD = Math.max(1, imp.getNSlices() / resamplingF);

			// ImageJのResizerを使用して平均化縮小を行う
			Resizer resizer = new Resizer();
			processedImp = resizer.zScale(imp, newD, ImageProcessor.NEAREST_NEIGHBOR); // Z方向
			// X-Y方向の縮小
			processedImp.getProcessor().setInterpolationMethod(ImageProcessor.NEAREST_NEIGHBOR);
			StackProcessor sp = new StackProcessor(processedImp.getStack());
			ImageStack resampledStack = sp.resize(newW, newH, true/*averaging when down sizing*/);
			processedImp.setStack(resampledStack);

			// キャリブレーションの修正（ボクセルサイズを resamplingF 倍にする）
			Calibration cal = processedImp.getCalibration();
			cal.pixelWidth *= resamplingF;
			cal.pixelHeight *= resamplingF;
			/*
			 * depthはresizer.zScaleですでに更新されている
			 */
//			cal.pixelDepth *= resamplingF;
		}

		// 2. パディング（縮小後の画像に対して行う）
		ImagePlus padding = zeroPad(processedImp);
		ImageStack stack = padding.getStack();

		int pW = padding.getWidth();
		int pH = padding.getHeight();
		int pD = padding.getNSlices();

		double dx = padding.getCalibration().pixelWidth;
		double dy = padding.getCalibration().pixelHeight;
		double dz = padding.getCalibration().pixelDepth;

		int scanStep = 1;
		for (int z = 0; z < pD - scanStep; z++) {
			for (int y = 0; y < pH - scanStep; y++) {
				for (int x = 0; x < pW - scanStep; x++) {
					processCube(stack, x, y, z, threshold, scanStep, dx, dy, dz);
				}
			}
		}
		return vertices;
	}

	private void processCube(ImageStack stack, int x, int y, int z, double thresh, int s, double dx, double dy,
			double dz) {
		// 1. キューブの8頂点の座標と値を定義
		// 頂点の並び順（インデックス）はアルゴリズム標準のルールに従う
		int[] px = { x, x + s, x + s, x, x, x + s, x + s, x };
		int[] py = { y, y, y + s, y + s, y, y, y + s, y + s };
		int[] pz = { z, z, z, z, z + s, z + s, z + s, z + s };

		double[] val = new double[8];
		for (int i = 0; i < 8; i++) {
			// ImageJのStackは1-based indexなので z+1
			val[i] = stack.getProcessor(pz[i] + 1).getf(px[i], py[i]);
		}

		// 2. どの頂点が閾値の内側にあるかを判定し、8ビットのインデックスを作成
		int cubeIndex = 0;
		if (val[0] >= thresh) cubeIndex |= 1;
		if (val[1] >= thresh) cubeIndex |= 2;
		if (val[2] >= thresh) cubeIndex |= 4;
		if (val[3] >= thresh) cubeIndex |= 8;
		if (val[4] >= thresh) cubeIndex |= 16;
		if (val[5] >= thresh) cubeIndex |= 32;
		if (val[6] >= thresh) cubeIndex |= 64;
		if (val[7] >= thresh) cubeIndex |= 128;

		// 3. 全て外側、または全て内側の場合は三角形を生成しない（早期リターン）
		if (edgeTable[cubeIndex] == 0)
			return;

		// 4. 辺上の交点（頂点）を計算
		// 辺の途中のどこに頂点を置くかを線形補間（interpolate）で決定
		Point3f[] vertList = new Point3f[12];
		if ((edgeTable[cubeIndex] & 1) != 0)
			vertList[0] = interpolate(thresh, px[0], py[0], pz[0], px[1], py[1], pz[1], val[0], val[1], dx, dy, dz);
		if ((edgeTable[cubeIndex] & 2) != 0)
			vertList[1] = interpolate(thresh, px[1], py[1], pz[1], px[2], py[2], pz[2], val[1], val[2], dx, dy, dz);
		if ((edgeTable[cubeIndex] & 4) != 0)
			vertList[2] = interpolate(thresh, px[2], py[2], pz[2], px[3], py[3], pz[3], val[2], val[3], dx, dy, dz);
		if ((edgeTable[cubeIndex] & 8) != 0)
			vertList[3] = interpolate(thresh, px[3], py[3], pz[3], px[0], py[0], pz[0], val[3], val[0], dx, dy, dz);
		if ((edgeTable[cubeIndex] & 16) != 0)
			vertList[4] = interpolate(thresh, px[4], py[4], pz[4], px[5], py[5], pz[5], val[4], val[5], dx, dy, dz);
		if ((edgeTable[cubeIndex] & 32) != 0)
			vertList[5] = interpolate(thresh, px[5], py[5], pz[5], px[6], py[6], pz[6], val[5], val[6], dx, dy, dz);
		if ((edgeTable[cubeIndex] & 64) != 0)
			vertList[6] = interpolate(thresh, px[6], py[6], pz[6], px[7], py[7], pz[7], val[6], val[7], dx, dy, dz);
		if ((edgeTable[cubeIndex] & 128) != 0)
			vertList[7] = interpolate(thresh, px[7], py[7], pz[7], px[4], py[4], pz[4], val[7], val[4], dx, dy, dz);
		if ((edgeTable[cubeIndex] & 256) != 0)
			vertList[8] = interpolate(thresh, px[0], py[0], pz[0], px[4], py[4], pz[4], val[0], val[4], dx, dy, dz);
		if ((edgeTable[cubeIndex] & 512) != 0)
			vertList[9] = interpolate(thresh, px[1], py[1], pz[1], px[5], py[5], pz[5], val[1], val[5], dx, dy, dz);
		if ((edgeTable[cubeIndex] & 1024) != 0)
			vertList[10] = interpolate(thresh, px[2], py[2], pz[2], px[6], py[6], pz[6], val[2], val[6], dx, dy, dz);
		if ((edgeTable[cubeIndex] & 2048) != 0)
			vertList[11] = interpolate(thresh, px[3], py[3], pz[3], px[7], py[7], pz[7], val[3], val[7], dx, dy, dz);

		// 5. triTable に基づいて三角形を形成し、リストに追加
		// triTable[cubeIndex] は -1 に出会うまで 3つ1組で頂点インデックスを返す
		for (int i = 0; triTable[cubeIndex][i] != -1; i += 3) {
			vertices.add(vertList[triTable[cubeIndex][i]]);
			vertices.add(vertList[triTable[cubeIndex][i + 1]]);
			vertices.add(vertList[triTable[cubeIndex][i + 2]]);
		}
	}

	// 頂点間の線形補間ロジック
	private Point3f interpolate(double t, int x1, int y1, int z1, int x2, int y2, int z2, double v1, double v2,
			double dx, double dy, double dz) {
		double mu = (Math.abs(v1 - v2) < 1e-10) ? 0.5 : (t - v1) / (v2 - v1);

		// 【重要】(x1 + mu*(x2-x1) - 1.0) の「-1.0」がパディング相殺
		float rx = (float) ((x1 + mu * (x2 - x1) - 1.0) * dx);
		float ry = (float) ((y1 + mu * (y2 - y1) - 1.0) * dy);
		float rz = (float) ((z1 + mu * (z2 - z1) - 1.0) * dz);

		return new Point3f(rx, ry, rz);
	}
	
	private ImagePlus zeroPad(final ImagePlus imp) {
		ImageStack stack = imp.getStack();
		int w = stack.getWidth();
		int h = stack.getHeight();
		int d = stack.getSize();
		int type = imp.getType();
		// create new stack 
		ImageStack st = new ImageStack(w+2, h+2);

		// retrieve 1st processor
		ImageProcessor old = stack.getProcessor(1);
		
		// enlarge it and add it as a first slide.
		ImageProcessor ne = createProcessor(w+2, h+2, type);
		st.addSlice("", ne);

		// now do the same for all slices in the old stack
		for(int z = 0; z < d; z++) {
			old = stack.getProcessor(z+1);
			ne = createProcessor(w+2, h+2, type);
			ne.insert(old, 1, 1);
			st.addSlice(Integer.toString(z+1), ne);
		}

		// now add an empty new slice
		ne = createProcessor(w+2, h+2, type);
		st.addSlice(Integer.toString(d+1), ne);

		ImagePlus dup = new ImagePlus();
		dup.setStack(null, st);

		// update the origin
		Calibration cal = imp.getCalibration().copy();
		cal.xOrigin -= cal.pixelWidth;
		cal.yOrigin -= cal.pixelHeight;
		cal.zOrigin -= cal.pixelDepth;
		dup.setCalibration(cal);
		
		return dup;
	}
	
	private final ImageProcessor createProcessor(int w, int h, int type) {
		if (type == ImagePlus.COLOR_RGB)
			return new ColorProcessor(w, h);
		return new ByteProcessor(w, h);
	}

